    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String holderName=policyHolderName.getText();
        int id=Integer.parseInt(policyID.getText());
        int numberOfClaims=Integer.parseInt(numberOfClaimsText.getText());
        int day=Integer.parseInt(dayText.getText());
        int month=Integer.parseInt(monthText.getText());
        int year=Integer.parseInt(yearText.getText());
        MyDate date= new MyDate(year,month,day);
        String carModel=carModelText.getText();
        int manufacturingYear=Integer.parseInt(manufacturingYearText.getText());
        int price=Integer.parseInt(priceText.getText());
        CarType carType=CarType.valueOf(carTypeComboBox.getSelectedItem().toString());
        Car car= new Car(carModel,carType,manufacturingYear,price);
        InsurancePolicy policy = null;

        if(thirdPartyPolicy.isSelected()){
            String comment=commentText.getText();
            try {
                policy= new ThirdPartyPolicy(holderName,id,car,numberOfClaims,date,comment);
            } catch (PolicyException ex) {
                Logger.getLogger(UserUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            int driverAge=Integer.parseInt(driverAgeText.getText());
            int level=Integer.parseInt(levelText.getText());
            try {
                policy= new ComprehensivePolicy(holderName,id,car,numberOfClaims,date,driverAge,level);
            } catch (PolicyException ex) {
                Logger.getLogger(UserUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(user.addPolicy(policy))
        {
            JOptionPane.showMessageDialog(this,"Policy Has been added");
            policyID.setText("");
            numberOfClaimsText.setText("");
            dayText.setText("");
            monthText.setText("");
            yearText.setText("");
            carModelText.setText("");
            manufacturingYearText.setText("");
            priceText.setText("");
            commentText.setText("");
            driverAgeText.setText("");
            levelText.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Policy couldn`t add");
        }
    }//GEN-LAST:event_addButtonActionPerformed

