    public void fillTable()
    {
        String [] header={"Policy Holder Name","policy ID","Car Model","Car Type","ManufacturingYear","Price","Number of Claims","Expiry Date","Comment","DriverAge","Level","Monthly Payment"};
        DefaultTableModel model=new DefaultTableModel(header,0);
        policiesInfoTable.setModel(model);
        for(InsurancePolicy policy: user.getPolicies().values())
        {
            String[] row=new String[12];
            row[0]=policy.getPolicyHolderName();
            row[1]=policy.getPolicyID()+"";
            row[2]=policy.getCar().getModel();
            row[3]=policy.getCar().getCarType().toString();
            row[4]=policy.getCar().getManufacturingYear()+"";
            row[5]=policy.getCar().getPrice()+"";
            row[6]=policy.getNumberOfClaims()+"";
            row[7]=policy.getExpiryDate().toString();
            if(policy instanceof ThirdPartyPolicy)
            {
                row[8]=((ThirdPartyPolicy)policy).getComment();
                row[9]="NA";
                row[10]="NA";
            }
            else
            {
                row[8]="NA";
                row[9]=((ComprehensivePolicy)policy).getDriverAge()+"";
                row[10]=((ComprehensivePolicy)policy).getLevel()+"";
            }
            String monthlyPayment=String.format("$%2f",policy.calcPremium(10));
            row[11]=monthlyPayment;
            model.addRow(row);
        }
    }
    
